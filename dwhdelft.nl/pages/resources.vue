        **Taalgebruik**
        1. Vermijd onnodig genderonderscheid zoals "Hé jongens en meisjes" of "Goedemorgen jongens". Probeer eens "Hé allemaal" of "Goedemorgen klas".
        2. Oefen iemands voornaamwoorden in je hoofd als die nog niet vanzelf gaan. Bijvoorbeeld voor een non-binair persoon: "Weet jij waar Ravi is?" "Ik zag hen vanmorgen, ik denk dat die vandaag in het lab werkt."
