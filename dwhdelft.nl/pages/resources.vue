        2. Noem je voornaamwoorden na je naam, in je email signature of wanneer je je voorstelt. Bijv. zij/haar, hij/hem of hij/die. Zo geef je iemand met niet-vanzelfsprekende voornaamwoorden de ruimte dit ook te doen, en het gevoel welkom te zijn.
        3. Interesse tonen is goed, maar check of je niet toevallig de tiende persoon die dag bent, en wees even terughoudend met vragen over medische ingrepen of genitaliën als je bij iedereen zou zijn. 4. Wijs mensen erop als ze kwetsende opmerkingen maken. Het is makkelijker voor jou als buitenstaander om op te komen voor een minderheid dan voor hen om voor zichzelf op te komen.
