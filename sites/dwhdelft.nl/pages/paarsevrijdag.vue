<script>
export default {
  async asyncData({ $content, app }) {
    return {
      faculties: (await $content('purple_faculties').fetch()).map((faculty, index) => ({
        ...faculty,
        active: index === 0,
      })),
    }
  },
  computed: {
    activeFaculty() {
      return this.faculties.find((faculty) => faculty.active)
    },
  },
  methods: {
    setActive(faculty) {
      this.faculties.forEach((faculty) => {
        faculty.active = false
      })
      faculty.active = true
    },
  },
}
